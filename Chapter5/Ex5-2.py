#ข้อนี้เหมือนข้อที่แล้ว 
#แต่มีสิ่งต้องตรวจสอบเพิ่มเติมคือ 
#ข้อมูลที่ต้องการลบอาจไม่มีอยู่ในลิสต์, index ที่ต้องการลบหรือเพิ่มข้อมูลอาจไม่ใช่ index ที่ถูกต้อง 
#ในกรณีที่พบเหตุการณ์เช่นนี้ในแสดงข้อความว่า Invalid
#A d คือนำ d ไปต่อท้ายลิสต์ x
#I j d คือแทรก d ที่ index j ใน x
#R data คือลบ d หนึ่งตัวออกจาก x
#P j คือลบตัวที่ index j ออกจาก x
x = ['a', 'bb', 'ccc', 'd', 'e']
c = input().split()
invalid = False
if c[0] == "A":
    x.append(c[1])
elif c[0] == "I":
    idx = int(c[1])
    if -(len(x)+1) <= idx <= len(x):
        x.insert(idx, c[2])
    else:
        invalid = True
elif c[0] == "R":
    if c[1] in x:
        x.remove(c[1])
    else:
        invalid = True
elif c[0] == "P":
    idx = int(c[1])
    if -len(x) <= idx < len(x):
        x.pop(idx)
    else:
        invalid = True
if not invalid: 
    print(x)
else:
    print("Invalid")

