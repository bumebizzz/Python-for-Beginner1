#โปรแกรมข้างล่างนี้มีลิสต์ d เก็บจำนวนเต็ม มีคำสั่งอ่านจำนวนเต็มเก็บใน q 
#จงเขียนคำสั่งต่อเพื่อหาว่า q เป็นตัวที่น้อยสุดอันดับที่เท่าไรใน d (กำหนดให้ตัวที่น้อยสุด ๆ ใน d คือตัวน้อยสุดอันดับ 1) ถ้าใน d ไม่มี q ให้แสดง Not found
d = [98,2,7,54,38,21,0,8,22,45,62]
q = int(input())
if q not in d:
    print("Not found")
else:
    d.sort()
    k = d.index(q)
    print(k+1)
