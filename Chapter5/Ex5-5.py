#กำหนดให้มีตัวแปร x เป็นลิสต์เก็บจำนวนเต็มมาให้แล้ว 
#จงนับว่าใน x มีข้อมูลคู่ที่ติดกันเหมือนกันอยู่กี่คู่ เช่น x = [1,1,1,1,2,2] มีติดกันหมือนกันอยู่ 4 คู่
x = input ()
c = 0
for i in range(len(x)-1):
    if x[i] == x[i+1]:
        c += 1
print(c)

