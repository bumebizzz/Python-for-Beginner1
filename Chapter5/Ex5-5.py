#กำหนดให้มีตัวแปร x เป็นลิสต์เก็บจำนวนเต็มมาให้แล้ว 
#จงตรวจว่า ข้อมูลใน x จากซ้ายไปขวา เรียงค่าจากน้อยไปมากหรือไม่ ถ้าใช่แสดง YES, ไม่ใช่ แสดง NO
x = input ()
for i in range(len(x)-1):
    if x[i] > x[i+1]:
        break
if len(x) > 1 and x[i] > x[i+1]:
    print("NO")
else:
    print("YES")