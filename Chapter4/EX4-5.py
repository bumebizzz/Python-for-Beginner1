#อ่านสตริงจาก input หนึ่งบรรทัด จากนั้นแสดงเฉพาะตัวอักขระในสตริงที่อยู่ที่ index คี่ (ตัวซ้ายสุดคือ index 0) บรรทัดละตัว 
#เช่น อ่านมาเป็น "*ABCDE*" จะแสดงแค่ A, C กับ E (บรรทัดละตัว)
t = input()
s = ""
for c in t[1::2]:
        print(c)
print(s)


