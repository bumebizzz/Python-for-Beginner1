#อ่านสตริงจาก input หนึ่งบรรทัด จากนั้นแสดงเฉพาะตัวอักษรอังกฤษในสตริงที่อ่านมาจากซ้ายไปขวา บรรทัดละตัว 
#เช่น อ่านมาเป็น "A-B-123 &" จะแสดงแค่ A กับ B (บรรทัดละตัว)
t = input()
s = ""
for c in t:
    if "a" <= c <= "z" or "A" <= c <= "Z":
        print(c)
print(s)

